module ODFWriter
  
  ########################################################################################
  #
  # SectionReader: find all sections and set name
  #
  ########################################################################################
  class SectionReader
    
    attr_accessor :name
    
    ######################################################################################
    #
    # initialize
    #
    ######################################################################################
    def initialize(opts)
      @name = opts[:name]
    end #def
    
    ######################################################################################
    #
    # sections
    #
    ######################################################################################
    def sections( doc )
      nodes( doc ).keys
    end #def
    
    ######################################################################################
    #
    # nodes
    #
    ######################################################################################
    def nodes( doc )
      doc.xpath(".//text:section").map{|node| [node.attr("text:name"), node] }.to_h
    end #def
    
  end #class
end #module
